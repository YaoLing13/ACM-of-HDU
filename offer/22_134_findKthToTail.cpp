/*
 * 题目: 输入一个链表, 输出该链表中倒数第k个节点
 * 思路: 定义2个指针
 */

#include <iostream>
#include "List.h"

// 1.输入空链表; 2.K>链表长度; 3.正常输入
ListNode* FindKthToTail(ListNode* pListHead, unsigned int k)
{
    //空链表
    if(pListHead == nullptr)
        return nullptr;

    //k>链表长度
    ListNode* pNodefast;
    ListNode* pNodeslow;
    pNodefast = pListHead;
    unsigned int count = 1;
    while(count < k && pNodefast->m_pNext != nullptr)    //pNodefast到达 k 处
    {
        ++count;
        pNodefast = pNodefast->m_pNext;
    }
    if(count != k)
        return nullptr;

    pNodeslow = pListHead;
    while(count == k && pNodefast->m_pNext != nullptr)    //pNodefast到达nullptr处
    {
        pNodefast = pNodefast->m_pNext;
        pNodeslow = pNodeslow->m_pNext;
    }
    return pNodeslow;
}

